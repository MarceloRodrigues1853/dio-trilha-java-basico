public class ExemploForArray {
    public static void main(String[] args) {
        // Declara e inicializa um array de Strings com os nomes dos alunos
        String alunos[] = { "FELIPE", "JONAS", "JULIA", "MARCOS" };

        // Loop for-each para percorrer o array alunos
        for (String aluno : alunos) {
            // Imprime o nome de cada aluno no array
            System.out.println("Nome do aluno é:" + aluno);
        }
        // Fim do loop for-each
    }
    // Fim do método main
}
// Fim da classe ExemploForArray
